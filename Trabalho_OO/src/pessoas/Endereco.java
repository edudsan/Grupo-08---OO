package pessoas;

public class Endereco {
	private String rua , bairro, cidade, estado;
	private int numero;
	
	public String getRua() {
		return rua;
	}
	public String getBairro() {
		return bairro;
	}
	public String getCidade() {
		return cidade;
	}
	public String getEstado() {
		return estado;
	}
	public int getNumero() {
		return numero;
	}
}
